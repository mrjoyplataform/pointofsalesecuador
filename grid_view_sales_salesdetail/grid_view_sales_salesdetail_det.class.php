                {
                    if(strcasecmp($_SESSION['sc_session'][$this->Ini->sc_page]['grid_view_sales_salesdetail']['fast_search'][2], $str_value) == 0)
                    {
                        $str_value = $str_html_ini. $str_value .$str_html_fim;
